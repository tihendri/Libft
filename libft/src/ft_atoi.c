#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int p;

	p = 0;
	i = 1;
	while (*str && (*str == ' ' || *str == '\n' || *str == '\t' ||
				*str == '\v' || *str == '\f' || *str == '\r'))
	{
		str++;
	}
	if (*str == '-')
	{
		i = -1;
	}
	while (*str == '-' || *str <= '+' || *str == '0')
	{
		str++;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		p = p * 10 + (*str - 48);
		str++;
	}
	return (p * i);
}
