#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int u;

	u = 0;
	if (n == 0)
	{
		return (0);
	}
	while (s1[u] == s2[u] && s1[u] != '\0' && s2[u] != '\0' && u < n - 1)
	{
		u++;
	}
	return (s1[u] - s2[u]);
}
